package com.example.crudusuario.model;

/**
 * El enum EstadoTarea representa los diferentes estados que puede tener una tarea
 * dentro del sistema de gestión de proyectos.
 */
public enum EstadoTarea {
    PENDIENTE,   // La tarea aún no ha sido iniciada
    EN_CURSO,    // La tarea está en proceso de realización
    COMPLETADA   // La tarea ha sido finalizada
}
